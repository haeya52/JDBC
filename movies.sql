drop table Movies cascade constraints;
drop table Ratings cascade constraints;

create table Movies(
  movieid varchar(10),
  title varchar(250),
  language varchar(10),
  production_company varchar(1000),
  production_country varchar(500),
  runtime INTEGER,
  release_date DATE,
  PRIMARY KEY (movieid));

create table Ratings(
  movieid varchar(10),
  userid varchar(10),
  rating numeric(2,1),
  PRIMARY KEY (movieid,userid),
  FOREIGN KEY (movieid) references Movies);

